	var 		db_user = process.env['DB_USERNAME'],
				db_pass = process.env['DB_PASSWORD'],
				db_url = db_user+':'+db_pass+'@langenium.com:27017/dev',
				Mongolian  = require("mongolian"),
				server = new Mongolian(db_url);

/*\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\
	Function Definitions
\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\*/

function get(path, search_term, callback) {
	var k;
	var collection = server.collection(path);
	collection.find(search_term).toArray(function(err, result) {
		if (err) { console.log(err); }
		else {
			callback(result);	
		}
	});
}






















