function make() {
	/* 
		Returns the standard container type instance object
		
		Parameters:
			none
	*/
	var	container = { 
									clock: setInterval( function(){ update(container); }, 1000 / 66),
									instances: []
								};
	return container;
}


function update(container) {
	container.instances.forEach(function(instance, instanceIndex) {
		instance.update();
	});
}